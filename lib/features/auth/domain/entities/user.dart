class User {
  final String id;
  final String email;
  final String role;

  final String username;
  final String profileImage;
  final String position;
  final int age;
  final String country;

  User({
    required this.id,
    required this.email,
    required this.role,
    required this.username,
    required this.profileImage,
    required this.position,
    required this.age,
    required this.country,
  });
}
